public class Dog {
    private String breed;
    private String gender;

    public Dog(String breed, String gender) {
        this.breed = breed;
        this.gender = gender;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
}
